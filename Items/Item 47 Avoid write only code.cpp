/*
 * Don't make "write-only" code
 * 
 * Write-only code seems straightforward when you write it, as you go from the specification to the code
 * However it is hard to read (either by somebody else or you in the future) as you can't easily go back from the code to the original specification
 * 
 * Write-only code is easy to write, hard to read!
 * 
 * 
 *
 * Summary:
 *  - Code is read more often than it is written
 *  - Code spends more time in maintenance than it does in development
 *  
 *  - So code to help both of those things!
 */